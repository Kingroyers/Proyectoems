package Modelo;

/**
 *
 * @author EDER
 */
public class Permisos {
    
    private int id;
    private String name;
    private int iden;
    private String job;
    private String type;
    private String description;

    public Permisos() {
    }

    public Permisos(int id, String name, int iden, String job, String type, String description) {
        this.id = id;
        this.name = name;
        this.iden = iden;
        this.job = job;
        this.type = type;
        this.description = description;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getIden() {
        return iden;
    }

    public void setIden(int iden) {
        this.iden = iden;
    }

    public String getJob() {
        return job;
    }

    public void setJob(String job) {
        this.job = job;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
    
}
